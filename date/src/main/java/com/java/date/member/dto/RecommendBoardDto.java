package com.java.date.member.dto;

public class RecommendBoardDto {
	private int recommend_code;
	private int board_num;
	private String recommend_nickName;
	private int recommend_check;
	
	public int getRecommend_code() {
		return recommend_code;
	}
	public void setRecommend_code(int recommend_code) {
		this.recommend_code = recommend_code;
	}
	public int getBoard_num() {
		return board_num;
	}
	public void setBoard_num(int board_num) {
		this.board_num = board_num;
	}
	public String getRecommend_nickName() {
		return recommend_nickName;
	}
	public void setRecommend_nickName(String recommend_nickName) {
		this.recommend_nickName = recommend_nickName;
	}
	public int getRecommend_check() {
		return recommend_check;
	}
	public void setRecommend_check(int recommend_check) {
		this.recommend_check = recommend_check;
	}
	
	
}
